    public function findByFilter(array $filter): array
    {
        $qb = $this->createQueryBuilder('c');

        if (isset($filter['name'])) {
            $qb->andWhere('c.name LIKE :name')
                ->setParameter('name', '%'.$filter['name'].'%');
        }

        return $qb->getQuery()->getResult();
    }

